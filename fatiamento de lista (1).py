lista = ["p", "y", "t", "h", "o", "n"]
# primeiro quadrato Ã© inicio, segundo  final e terceiro pula de quanto em quanto step
print(lista[2:])  # ["t", "h", "o", "n"]
print(lista[:2])  # ["p", "y"]
print(lista[1:3])  # ["y", "t"]
print(lista[0:3:2])  # ["p", "t"]
print(lista[::])  # ["p", "y", "t", "h", "o", "n"]
print(lista[::-1])  # ["n", "o", "h", "t", "y", "p"]